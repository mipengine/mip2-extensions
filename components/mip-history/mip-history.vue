<template>
  <div
    :class="defaultClass"
    @click="closeBanner"
  >
    <slot/>
  </div>
</template>

<script>
export default {
  props: {
    history: {
      type: String,
      default: null
    },
    defaultClass: {
      type: String,
      default: ''
    }
  },
  created () {
    this.initDefaultClass(this.$element)
  },
  methods: {
    initDefaultClass ($element) {
      if ($element &&
        $element.className &&
        $element.className.indexOf &&
        $element.className.indexOf('mip-history-default') > -1
      ) {
        this.defaultClass = 'mip-history-default'
      }
    },
    closeBanner () {
      if (this.history) {
        let historyArr = this.history.split(',')
        let func = historyArr[0]
        switch (func) {
          case 'go':
            let step = historyArr[1]
            if (step) {
              window.history.go(step - 0)
            } else {
              console.warn('history.go() 需要填写第二个参数')
            }
            break
          case 'back':
            window.history.back()
            break
          case 'forward':
            window.history.forward()
            break
        }
      }
    }
  }
}
</script>

